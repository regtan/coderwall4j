package org.kirino.coderwall4j.model;

public class TeamAchievement {
	private Long achievementNo;
	private String achievementName;
	private String achievementImgSourceUrl;

	public Long getAchievementNo() {
		return achievementNo;
	}

	public void setAchievementNo(Long achievementNo) {
		this.achievementNo = achievementNo;
	}

	public String getAchievementName() {
		return achievementName;
	}

	public void setAchievementName(String achievementName) {
		this.achievementName = achievementName;
	}

	public String getAchievementImgSourceUrl() {
		return achievementImgSourceUrl;
	}

	public void setAchievementImgSourceUrl(String achievementImgSourceUrl) {
		this.achievementImgSourceUrl = achievementImgSourceUrl;
	}

}
